# Loading the training data
PATH = os.getcwd()

# Define data path
data_path = PATH + '/NDatasets'
data_dir_list = os.listdir(data_path)

img_data_list=[]

for dataset in data_dir_list:
	img_list=os.listdir(data_path+'/'+ dataset)
	print ('Loaded the images of dataset-'+'{}\n'.format(dataset))
	for img in img_list:
		img_path = data_path + '/'+ dataset + '/'+ img
		img = image.load_img(img_path, target_size=(299, 299))
		x = image.img_to_array(img)
		x = np.expand_dims(x, axis=0)
		x = preprocess_input(x)
#		x = x/255
		print('Input image shape:', x.shape)
		img_data_list.append(x)

img_data = np.array(img_data_list)
#img_data = img_data.astype('float32')
print (img_data.shape)
img_data=np.rollaxis(img_data,1,0)
print (img_data.shape)
img_data=img_data[0]
print (img_data.shape)


# Define the number of classes
num_classes = 5
num_of_samples = img_data.shape[0]
labels = np.ones((num_of_samples,),dtype='int64')

labels[0:907]=0
labels[907:1923]=1
labels[1923:2944]=2
labels[2944:3872]=3
labels[3872:]=4


names = ['bad','fair','good','poor','v_bad']

# convert class labels to on-hot encoding
Y = np_utils.to_categorical(labels, num_classes)

#Shuffle the dataset
x,y = shuffle(img_data,Y, random_state=2)
# Split the dataset
X_train, X_test, y_train, y_test = train_test_split(x, y, test_size=0.4, random_state=2)

#Model
#Training the feature extraction also

#image_input = Input(shape=(224, 224, 3))
pre_trained_model = tf.keras.applications.Xception(input_shape = (299, 299, 3), # Shape of our images
                                include_top = False, # Leave out the last fully connected layer
                                weights = 'imagenet')



for layer in pre_trained_model.layers:
  layer.trainable = False

x = pre_trained_model.output
x = GlobalAveragePooling2D()(x)
x = Dense(1024, activation='relu')(x)                  
# Add a final sigmoid layer for classification
x = layers.Dense  (5, activation='softmax')(x)           

model = Model( pre_trained_model.input, x) 
model.summary()

model.compile(optimizer = Adam(lr=0.0001), 
              loss = 'categorical_crossentropy', 
              metrics = ['acc'])


t=time.time()
#	t = now()


model.compile(loss='categorical_crossentropy',optimizer='adam',metrics=['accuracy'])

from keras import callbacks
earlystopping=callbacks.EarlyStopping(monitor="val_loss",mode="min",patience=5,restore_best_weights=True)

hist = model.fit(X_train, y_train, batch_size=32, epochs=20, verbose=1, validation_data=(X_test, y_test),
                             callbacks=[earlystopping]
                             )
print('Training time: %s' % (t - time.time()))
(loss, accuracy) = model.evaluate(X_test, y_test, batch_size=10, verbose=1)

print("[INFO] loss={:.4f}, accuracy: {:.4f}%".format(loss,accuracy * 100))

#%%
